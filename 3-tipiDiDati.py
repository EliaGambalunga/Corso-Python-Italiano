#Python ha diversi tipi di dati, per capire di che tipo di dato si tratta
#si usa la funzione type()
print(type(1))  
# nei dati numerici ci sono gli interi (int) e i float (numeri decimali)
x = 1.5 # float
x = 5 #int
# ci sono anche i tipi di dati testuali che si indiacano con le stringhe (str), si riconoscono perché sono tra virgolette
x = "ciao"
# le stringhe possono essere tra virgolette singole o doppie
x = 'ciao'
# ci sono anche i tipi di dati booleani (bool) che possono essere True o False
x = True
x = False
#ci sono anche le liste che sono sequenze di dati, si indicano con le parentesi quadre
x = [1, 2, 3, 4, 5] # lista
# le liste possono contenere anche altri tipi di dati
x = [1, "ciao", True, 3.14] # lista con diversi tipi di dati
