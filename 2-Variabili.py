#Una variabile è un contenitore che può contenere un valore.
#Le variabili in Python non hanno un tipo di dato specifico, ma possono contenere
#diversi tipi di dati come numeri, stringhe, liste, dizionari...
x = 10  # Una variabile intera
#una variabile deve per forza essere assegnata ad un valore, altrimenti non può essere utilizzata
#chiameremo il nome delle variabili con questa logica: 
pesoPersona = 70
#no trattini, spazi, numeri all'inizio del nome.
#si possono assegnare valori multipli a più variabili in una sola riga
x, y, z = 1, 2, 3  # Assegnazione multipla, separati da virgola
print(x)
print(y)
print(z)
#le variabili possono essere modificate in qualsiasi momento
a, b, c = 4 #più variabili con lo stesso valore
print(a)
print(b)
print(c)
#posso fare anche operazioni con le variabili
x = 5
y = 10
z = x + y  # Somma delle variabili
print(z)  # Stampa il risultato della somma